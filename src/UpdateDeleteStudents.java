    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Action Performed code for the update button
        String mobileNumber = jTextField1.getText();
        String name = jTextField2.getText();
        String fatherName = jTextField3.getText();
        String motherName = jTextField4.getText();
        String email = jTextField5.getText();
        String address = jTextField6.getText();
        String college = jTextField7.getText();
        String enrollment = jTextField8.getText();
        String roomNumber = jTextField9.getText();
        String status = (String)jComboBox1.getSelectedItem();
        
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            if (status.equals("living")) {
                st.executeUpdate("update room set roomStatus='Booked' where number='"+roomNumber+"'");
            } else {
                st.executeUpdate("update room set roomStatus='Not Booked' where number='"+roomNumber+"'");
            }
            PreparedStatement ps = con.prepareStatement("update student set name=?,father=?,mother=?,email=?,address=?,college=?,enrollment=?,status=? where mobileNo=?");
            ps.setString(1, name);
            ps.setString(2, fatherName);
            ps.setString(3, motherName);
            ps.setString(4, email);
            ps.setString(5, address);
            ps.setString(6, college);
            ps.setString(7, enrollment);
            ps.setString(8, status);
            ps.setString(9, mobileNumber);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully update");
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // ActionPerformed code for the delete student button
        String mobileNumber = jTextField1.getText();
        String roomNumber = jTextField9.getText();
        
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("delete from student where mobileNo='"+mobileNumber+"'");
            st.executeUpdate("update room set roomStatus='Not Booked' where number='"+roomNumber+"'");
            JOptionPane.showMessageDialog(null, "Successfully Deleted");
            clear();
        } catch(Exception e) {
           JOptionPane.showMessageDialog(null, e); 
        }
    }//GEN-LAST:event_jButton4ActionPerformed

